#include<bits/stdc++.h>
#define ll long long

using namespace std;

int main(){
    ios_base::sync_with_stdio(false);
    cin.tie(NULL);
    int t;cin>>t;
    while(t--){
        int n; cin>>n;
        vector<pair<ll, ll>> p;
        for(int i = 0; i < n; i++) {
            int a,b; cin>>a>>b;
            p.push_back(make_pair(a, b));
        }
    }
}
